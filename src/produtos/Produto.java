package produtos;


public class Produto {
    
    private String codigo_pro;
    private String tipo_pro;
    private String nome_pro;
    private String valor_pro;

    /**
     * @return the codigo_pro
     */
    public String getCodigo_pro() {
        return codigo_pro;
    }

    /**
     * @param codigo_pro the codigo_pro to set
     */
    public void setCodigo_pro(String codigo_pro) {
        this.codigo_pro = codigo_pro;
    }

    /**
     * @return the tipo_pro
     */
    public String getTipo_pro() {
        return tipo_pro;
    }

    /**
     * @param tipo_pro the tipo_pro to set
     */
    public void setTipo_pro(String tipo_pro) {
        this.tipo_pro = tipo_pro;
    }

    /**
     * @return the nome_pro
     */
    public String getNome_pro() {
        return nome_pro;
    }

    /**
     * @param nome_pro the nome_pro to set
     */
    public void setNome_pro(String nome_pro) {
        this.nome_pro = nome_pro;
    }

    /**
     * @return the valor_pro
     */
    public String getValor_pro() {
        return valor_pro;
    }

    /**
     * @param valor_pro the valor_pro to set
     */
    public void setValor_pro(String valor_pro) {
        this.valor_pro = valor_pro;
    }
    
}
